# # 셀참조
#
# - 상대참조
#     - B5 와 같은 것
#         - 채우기 핸들 또는 셀을 복사해서 특정 위치에 붙여넣기하면 이동 만큼 참조위치가 변함
#     
# - 절대참조(F4 눌러서 조정)
#     - '$달러B$달러5'
#         - 채우기 핸들 또는 셀을 복사해서 특정 위치에 붙여넣기 해도 항상 동일한 셀/범위를 참조
#         - 시트이름!셀 (참조할 셀이 다른 시트인경우)
#            (ex > 기본자료!달러c달러3)
#         - 다른 셀에 이름상자에 이름 정의하면 수식에서 사용가능
#         - = 셀 * F3(내가 저장한 셀 이름들 나옴/ 선택)
#     
# - 혼합참조
#     - '$달러B5,B$달러5'
#         - 셀 참조시 행 또는 열 하나는 상대 다른 하나는 절대 참조 형식
#         - 마우스로 드래그하면 서식이 깨짐
#         - 따라서 동일한 수식을 적용하는 방법은 범위를 선택 적용 수식 셀 선택 후 ctrl + Enter

