# # 수식에 대해 알아보기
#
# - 수식 입력시 : = 입력을 시작으로 한다
#     - = 2+1 --------> 3 출력(B3 이라고 가정)
#     - 셀참조를 활용할 수 있음
#         - = B3 + 1(수식입력줄에 보이는 형태) ------->4 
#         - = B3 + B5 -----> 7
#         - 문자 데이터는 산술 연산 불가능 (#VALUE! 가 나타남)
#         - = B4 & B5 ( & 문자열 연결 연산 : 두개의 데이터를 연결하여 문자로 만들어줌) -------> 34(문자열)
#         - ="50" (등호로 시작하고 "" 안에 입력하면 문자 데이터로 인식)
#         - 특이한 것은 50 은 문자지만 숫자와 산술연산이 가능하고 숫자로 인식
#         - 맨처음 = 수식임을 나타내고, 다음 등호는 같다 라는 것을 나타냄
#         - ="50"=50-----------> False ( 앞의 것 문자 뒤의 것 숫자)
#         - ="50"*1 = 50 ---->True
#       
# - 연산의 종류 ( 순서대로 우선순위 )
#
#  - 참조연산
#  - 산술연산
#  - 문자열연산
#  - 비교연산  : T/F ---->비교대상/기준값을 넣음( 결과는 T/F )
#  - 함수식 연산

